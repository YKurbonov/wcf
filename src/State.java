public class State {

    private String STATE;
    private String MIN_BALANCE;
    private String MAX_BALANCE;
    private String  MEAN_BALANCE;
    private String  TOTAL_BALANCE;

    public String getSTATE() {
        return STATE;
    }

    public void setSTATE(String STATE) {
        this.STATE = STATE;
    }

    public String getMIN_BALANCE() {
        return MIN_BALANCE;
    }

    public void setMIN_BALANCE(String MIN_BALANCE) {
        this.MIN_BALANCE = MIN_BALANCE;
    }

    public String getMAX_BALANCE() {
        return MAX_BALANCE;
    }

    public void setMAX_BALANCE(String MAX_BALANCE) {
        this.MAX_BALANCE = MAX_BALANCE;
    }

    public String getMEAN_BALANCE() {
        return MEAN_BALANCE;
    }

    public void setMEAN_BALANCE(String MEAN_BALANCE) {
        this.MEAN_BALANCE = MEAN_BALANCE;
    }

    public String getTOTAL_BALANCE() {
        return TOTAL_BALANCE;
    }

    public void setTOTAL_BALANCE(String TOTAL_BALANCE) {
        this.TOTAL_BALANCE = TOTAL_BALANCE;
    }
}
